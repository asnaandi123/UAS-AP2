    public void selectBynm(int nm) throws SQLException {
        koneksi konek = new koneksi();
        konek.koneksi();

        try {
            String sql = "select * from datasantri where nomorsantri like '%" + nm + "%'";
            pst = konek.con.prepareStatement(sql);
            rs = pst.executeQuery(sql);
            adm_tabel.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }


    private void adm_tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_adm_tabelMouseClicked
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel) adm_tabel.getModel(); //create a model
        int selectedRowIndex = adm_tabel.getSelectedRow(); //get selected row
        int id = (int) dtm.getValueAt(selectedRowIndex, 0); //get the primary key to fetch data.

        koneksi konek = new koneksi();
        konek.koneksi();

        try {

            String sql = "select * from datasantri where nomorsantri like '%" + id + "%'";
            pst = (Statement) konek.con.prepareStatement(sql);
            ResultSet rs = rs = pst.executeQuery(sql);

            while (rs.next()) {    //u can use normal query
                in_nm.setText(rs.getString("nomorsantri"));
                in_ang.setText(rs.getString("angkatan"));
                in_alm.setText(rs.getString("alamat"));
                in_ns.setText(rs.getString("namasantri"));
                in_jns.setText(rs.getString("jeniskelamin"));
            }
        } catch (Exception ex) {
        }

    }//GEN-LAST:event_adm_tabelMouseClicked

